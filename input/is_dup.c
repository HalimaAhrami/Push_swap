#include "../push_swap.h"

int	is_dup(int *a, int n)
{
	int	i;
	int	j;
	int	dup;

	i = 0;
	while (i < n)
	{
		dup = a[i];
		j = i + 1;
		while (j < n)
		{
			if (dup == a[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
