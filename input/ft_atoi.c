#include "../push_swap.h"

int	ft_atoi(char *str, int *err)
{
	int		i;
	int		s;
	long	r;

	(1) && (i = 0), (s = 1), (r = 0);
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	if (!str[i])
		*err = 1;
	while (str[i] == '0')
		i++;
	if (ft_strlen(str + i) > 11)
		*err = 1;
	while (str[i] >= '0' && str[i] <= '9' && str[i])
	{
		r = r * 10 + (str[i] - '0');
		if (r * s > 2147483647 || r * s < -2147483648)
			*err = 1;
		i++;
	}
	return (s * r);
}
